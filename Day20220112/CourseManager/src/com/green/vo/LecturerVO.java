package com.green.vo;

public class LecturerVO {
	private int l_idx;
	private String l_name;
	private String l_major;
	private String field;
	
	public int getL_idx() {
		return l_idx;
	}
	public void setL_idx(int l_idx) {
		this.l_idx = l_idx;
	}
	public String getL_name() {
		return l_name;
	}
	public void setL_name(String l_name) {
		this.l_name = l_name;
	}
	public String getL_major() {
		return l_major;
	}
	public void setL_major(String l_major) {
		this.l_major = l_major;
	}
	public String getField() {
		return field;
	}
	public void setField(String field) {
		this.field = field;
	}
}
